    private void loadTB() {
        File f = new File(fTB);
        if (f.exists()) {
            list1 = IOFile.read(fTB);
        } else {
            IOFile.write(fTB, list1);
        }
    }

    private void loadPH() {
        File f = new File(fPH);
        if (f.exists()) {
            list2 = IOFile.read(fPH);
        } else {
            IOFile.write(fPH, list2);
        }
    }

    private void loadQL() {
        File f = new File(fQL);
        if (f.exists()) {
            list3 = IOFile.read(fQL);
        } else {
            IOFile.write(fQL, list3);
        }
    }

    private void showTB() {
        tmTB.setRowCount(0);
        for (ThietBi i : list1) {
            tmTB.addRow(i.toObjects());
        }
    }

    private void showPH() {
        tmPH.setRowCount(0);
        for (PhongHoc i : list2) {
            tmPH.addRow(i.toObjects());
        }
    }

    private void showQL() {
        tmQL.setRowCount(0);
        for (QLTB i : list3) {
            tmQL.addRow(i.toObjects());
        }
    }

    private boolean trung(int maPH, int maTB) {
        for (QLTB i : list3) {
            if (i.getPh().getMa() == maPH && i.getTb().getMa() == maTB) {
                return true;
            }
        }
        return false;
    }

    private void loadMa() {
        maPHCB.removeAllItems();
        maTBCb.removeAllItems();
        for (ThietBi i : list1) {
            maPHCB.addItem(i.getMa() + "");
        }
        for (PhongHoc i : list2) {
            maTBCb.addItem(i.getMa() + "");
        }

    }

