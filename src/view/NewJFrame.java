    private void sortCBItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_sortCBItemStateChanged
        if (sortCB.getSelectedIndex() == 1) {
            list3.sort(new Comparator<QLTB>() {
                @Override
                public int compare(QLTB o1, QLTB o2) {
                    String[] ten1 = o1.getPh().getTen().split("\\s+");
                    String[] ten2 = o2.getPh().getTen().split("\\s+");
                    if (ten1[ten1.length - 1].equals(ten2[ten2.length - 1])) {
                        return o1.getPh().getTen().compareToIgnoreCase(o2.getPh().getTen());
                    } else {
                        return ten1[ten1.length - 1].compareToIgnoreCase(ten2[ten2.length - 1]);
                    }
                }
            });
        }
        if (sortCB.getSelectedIndex() == 2) {
            Collections.sort(list3, new Comparator<QLTB>() {
                @Override
                public int compare(QLTB o1, QLTB o2) {
                    return o1.getSluong() - o2.getSluong();
                }
            });
            Collections.reverse(list3);
        }
        tmQL.setRowCount(0);
        for (QLTB i : list3) {
            tmQL.addRow(i.toObjects());
        }

    }//GEN-LAST:event_sortCBItemStateChanged

